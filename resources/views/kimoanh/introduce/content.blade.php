                <h1 class="title-news">
                Thông điệp của Tổng Giám Đốc</h1>

                <div class="newsdt-content">
                  <div style='font-weight:bold'><br /></div><br /><div style="text-align: right;">
                    <div>
                      <div>
                        <div><br />
                        </div>
                        <div style="text-align: left;"><img alt="" width="355" height="270" data-cke-saved-src="http://www.diaockimoanh.com.vn/images/news/article/CompanyProfile/Tong%20giam%20doc%20nguyen%20thi%20kim%20oanh.jpg" src="../images/news/article/CompanyProfile/Tong%20giam%20doc%20nguyen%20thi%20kim%20oanh.jpg" style="text-align: justify; margin: 6px 12px; float: left;" />
                        </div>
                      </div>
                      <div style="text-align: justify;">Sau hơn sáu năm chính thức thành lập,
                        đến nay <strong>Công ty cổ phần Dịch vụ Thương mại và Xây dựng Địa ốc Kim Oanh</strong>
                        đã đạt được một bước tiến dài trong hành trình khẳng định thương hiệu
                        trên thị trường bất động sản. Từ vài nhân viên ban đầu, hiện nay <strong>Kim
                        Oanh</strong> đã phát triển thành hệ thống gồm sáu chi nhánh tại Bình Dương,
                        TPHCM và Đồng Nai với đội ngũ gần 600 nhân viên được đào tạo bài bản, am
                      hiểu thị trường bất động sản. </div>
                      <div style="text-align: justify;"><br />
                      </div>
                      <div style="text-align: justify;">Với phương châm kinh doanh “Sản phẩm
                        thật – Giá trị thật”, thời gian vừa qua, mỗi năm công ty phân phối thành
                        công đến tay khách hàng khoảng 3.000 sản phẩm thuộc nhiều phân khúc thị
                        trường khác nhau. Mỗi một sản phẩm được Kim Oanh lựa chọn, phát triển
                        và phân phối đều chứa đựng nhiều tâm huyết, tọa lạc ở những vị trí đắc
                        địa, pháp lý minh bạch, giàu tiềm năng gia tăng giá trị nhưng giá bán
                      hợp lý.</div>
                      <div style="text-align: justify;"><br />
                      </div>
                      <div style="text-align: justify;">Từ cuối năm 2015, Kim Oanh đã thực
                        hiện một bước ngoặt mới khi chính thức thành lập Kim Oanh Group gồm năm đơn vị thành viên: <strong>Công ty cổ phần Dịch vụ Thương mại và Xây dựng Địa ốc
                        Kim Oanh</strong>, <strong>Công ty cổ phần Đầu tư và Phát triển Thuận Lợi</strong>, <strong>Công ty cổ
                        phần Đầu tư và Phát triển Phú Thuận Lợi</strong>, <strong>Công ty TNHH Xây dựng An Phước
                        Bình Dương,&nbsp;Công ty TNHH A Đông Hải&nbsp;</strong>. Sự kiện này không chỉ là dấu ấn minh chứng cho sự lớn mạnh
                        của thương hiệu Kim Oanh, giúp công ty hoạt động chuyên nghiệp hơn, nắm
                        bắt cơ hội thị trường tốt hơn mà còn mang đến nhiều lợi ích cho khách
                        hàng và thị trường bất động sản. Đặc biệt, <strong>Kim Oanh Group</strong> sẽ chuyển mạnh
                        sang vai trò chủ đầu tư các dự án khu đô thị quy mô lớn để đảm bảo chất
                        lượng xây dựng và cung cấp cho thị trường những sản phẩm đúng giá trị
                      thật.</div>
                      <div style="text-align: justify;"><br />
                      </div>
                      <div style="text-align: justify;">Cùng với bước tiến phát triển mới,
                        hiện nay danh sách đối tác chiến lược của <strong>Kim Oanh Group</strong> cũng đã dài
                        thêm với những tên tuổi lớn như Becamex, Tín Nghĩa, Thanh Lễ, Giang
                        Điền, ATA, DIC Corp., Bái Tử Long. Bên cạnh đó, Kim Oanh cũng thắt chặt
                        quan hệ hợp tác đồng hành với các tổ chức tài chính hàng đầu như
                        Vietcombank, BIDV, Agribank, Sacombank… để hẫu thuận cho việc phát triển
                        dự án và tạo điều kiện tốt nhất cho khách hàng sở hữu sản phẩm bất động
                        sản do công ty đầu tư , phân phối.&nbsp; Mặt khác, <strong><a href="http://www.diaockimoanh.com.vn/">Kim Oanh Group</a> </strong>còn đặt
                        mục tiêu ít nhất sẽ đưa cổ phiếu một đơn vị thành viên niêm yết trên thị
                        trường chứng khoán vào năm 2018 để mở rộng khả năng huy động vốn và tìm
                      kiếm cơ hội hợp tác với các nhà đầu tư lớn, chuyên nghiệp.</div>
                      <div style="text-align: justify;"><br />
                      </div>
                      <div style="text-align: justify;">Tại thời điểm này, những dấu ấn đầu
                        tiên trong vai trò chủ đầu tư phát triển dự án của Kim Oanh Group cũng
                        đã được các cấp chính quyền và xã hội ghi nhận. Có thể kể đến như khu đô
                        thị thương mại dịch vụ RichHome, khu đô thị thương mại RichHome 2 tại
                        Bình Dương. Các dự án này được quy hoạch chỉn chu, xây dựng chất lượng,
                        đầy đủ tiện ích, không chỉ góp phần chỉnh trang đô thị tại địa phương mà
                        còn giúp cho nhiều người lao động thỏa mãn giấc mơ sở hữu nhà ở. Đặc
                        biệt, đây cũng là những dự án nhà phố xây sẵn đầu tiên tại Bình Dương
                        được ngân hàng hợp tác với Kim Oanh Group cho người mua vay gói 30.000
                      tỉ đồng với lãi suất ưu đãi.</div>
                      <div style="text-align: justify;"><br />
                      </div>
                      <div style="text-align: justify;">Hiện nay, <strong>Kim Oanh Group</strong> đã tích lũy
                        quỹ đất sạch lên đến hơn 250 héc ta tại các thị trường trọng điểm như
                        Bình Dương, TPHCM và Đồng Nai. Sắp tới, lần đầu tiên Kim Oanh Group sẽ
                        công bố ra thị trường một loạt dự án khu đô thị thương mại – dịch vụ cao
                        cấp, xây dựng theo mô hình khép kín với đầy đủ tiện ích hiện đại, từ
                        trung tâm thương mại, chợ, khu vui chơi giải trí cho đến trường học,
                        trung tâm thể dục thể thao, trung tâm ẩm thực... phục vụ nhu cầu đang
                        tăng cao của người dân lẫn các nhà đầu tư bất động sản. Có thể kể đến
                        như dự án trọng điểm như Golden Center City, Golden Center City 2,
                        Golden Riverside&nbsp; hay An Điền với quy mô hàng ngàn sản phẩm. Các dự án
                        này không chỉ đáp ứng nhu cầu an cư mà còn được kỳ vọng trở thành đầu
                        mối giao thương cho các doanh nhân, chuyên gia đến tìm kiếm cơ hội hợp
                      tác làm ăn và trải nghiệm sản phẩm, dịch vụ chất lượng cao. </div>
                      <div style="text-align: justify;"><br />
                      </div>
                      <div style="text-align: justify;">Trong quá trình hoạt động, bên cạnh
                        kinh doanh, Kim Oanh Group cũng không quên đóng góp cho cộng đồng. Tính
                        đến nay, công ty đã dành ra 21,5 tỉ đồng thực hiện các chương trình trao
                        tặng học bổng, xây tặng nhà tình nghĩa, nhà tình thương, phụng dưỡng
                        các bà mẹ Việt Nam anh hùng, bảo trợ các mái ấm tình thương, xây cầu
                      nông thôn, hỗ trợ các chiến sĩ làm nhiệ vụ bảo vệ tổ quốc, biển đảo… </div>
                      <div style="text-align: justify;"><br />
                      </div>
                      <div style="text-align: justify;">Đặc biệt hơn, từ tháng 5-2016, Kim
                        Oanh Group&nbsp; đã chính thức thành lập Quỹ từ thiện Kim Oanh nhằm chuyên
                        nghiệp hóa và nâng cao hiệu quả của các công tác xã hội. Với số tiền 6,7
                        tỉ đồng do Kim Oanh Group và công đoàn đóng góp ban đầu dành riêng cho
                        năm 2016, Quỹ từ thiện Kim Oanh sẽ tập trung vào các chương trình lớn
                        như: Đỡ đầu các em học sinh nghèo hiếu học; Chăm lo, hỗ trợ cho các cán
                        bộ chiến sĩ và con em của họ; Hỗ trợ nạn nhân chất độc da cam; Hỗ trợ
                        người bị tai nạn lao động nhằm chia sẻ và kịp thời giúp các nạn nhân
                        vượt qua khó khăn... Thông qua các chương trình này, hy vọng sẽ có thêm
                        nhiều Mạnh Thường Quân, các cấp chính quyền, các tổ chức cùng chung tay
                        góp sức mang trái tim yêu thương giúp cho nhiều mảnh đời bất hạnh hơn
                      nữa. </div>
                      <div style="text-align: justify;"><br />
                      </div>
                      <div style="text-align: justify;">Đó cũng chính là mục đích cuối cùng, là tâm nguyện của những người đã góp phần tạo dựng nên thương hiệu Kim Oanh.</div>
