			<div class="center-news">
				<div style='font-weight:bold'><div style="text-align: justify;">Theo quy hoạch xây dựng vùng TP.Hồ Chí Minh thì huyện Trảng Bom, Long Thành, Nhơn Trạch của Đồng Nai sẽ trở thành trung tâm logistics của vùng. Do đó, quy hoạch sử dụng đất của 3 khu vực trên sẽ dựa trên quy hoạch vùng để thực hiện nhằm kết nối giao thông trong vùng, phát triển kho, cảng trung chuyển hàng hóa.</div></div><br /><div style="text-align: center;"><img alt="" src="../../../images/news/article/tin-tuc/2018/images2120117_1_cang_NT.jpg" /><br />
