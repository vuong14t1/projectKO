				</div>
				<div style="text-align: center;"><em>Khu vực Nhơn Trạch sẽ là nơi phát triển cảng đường thủy và là trung tâm logistics vùng.<br />
				Trở thành trung tâm logistics vùng, các huyện Trảng Bom, Long Thành, Nhơn Trạch sẽ phát triển cảng, kho bãi, vận tải đường hàng không, đường bộ, đường sắt, đường thủy.</em><br />
				<br />
				<div style="text-align: justify;"><strong>Hàng ngàn hécta đất làm logistics</strong></div>
				<div style="text-align: justify;"><br />
				</div>
				<div style="text-align: justify;">Quy hoạch sử dụng đất để phát triển giao thông, logistics của 3 địa phương này đều được tỉnh dựa vào quy hoạch vùng để thực hiện. 3 huyện trên đều là địa phương có nhiều đường giao thông lớn đi qua nên phát triển kho bãi và các dịch vụ liên quan đi kèm sẽ rất phù hợp và thuận lợi.</div>
				<div style="text-align: justify;"><br />
				</div>
				<div style="text-align: justify;">
					<table style="background-color: #ffc000;">
						<tbody>
							<tr>
								<td>Theo UBND tỉnh, Đồng Nai là nơi có rất nhiều đường giao thông lớn đi qua như: cao tốc TP.Hồ Chí Minh - Long Thành - Dầu Giây, Bến Lức - Long Thành, quốc lộ 1, quốc lộ 51, quốc lộ 20, đường sắt Bắc - Nam,...hệ thống sông Đồng Nai rất thuận lợi cho phát triển&nbsp; giao thông đường bộ, đường sắt, đường thủy. Vì vậy, quy hoạch Trảng Bom - Long Thành - Nhơn Trạch là trung tâm logistics vùng sẽ tạo động lực phát triển kinh tế khu vực phía Nam.&nbsp;</td>
							</tr>
						</tbody>
					</table>
					<br />
				</div>
				<div style="text-align: justify;">Ông Trần Văn Danh, Chủ tịch Hội đồng thành viên Công ty TNHH giao nhận vận tải Lục Phát (TP.Biên Hòa), cho hay: “Đồng Nai là cửa ngõ giao thông của nhiều tỉnh, thành phía Nam nên làm trung tâm logistics vùng rất thuận lợi. Tới đây khi Cảng hàng không quốc tế Long Thành hoàn thành và đi vào hoạt động sẽ là nơi trung chuyển hàng hóa bằng đường hàng không lớn nhất cả nước”.<br />
					<br />
					Trong phê duyệt kế hoạch sử dụng đất, 3 địa phương trên cũng đã dành quỹ đất lớn lên đến gần 6.900 hécta để phát triển giao thông và các dự án logistics. Trong đó, huyện Long Thành hơn 5.200 hécta, Nhơn Trạch gần 1.200 hécta, Trảng Bom gần 500 hécta.<br />
					<br />
					Phó chủ tịch UBND huyện Trảng Bom Lê Hữu Đảng cho biết: “Theo quy hoạch, huyện có cảng kho khoảng 800 hécta. Sau này nơi đây sẽ là khu trung chuyển hàng hóa lớn của vùng. Hiện ga đường sắt khu vực huyện Trảng Bom đã được xây dựng, mở rộng có thể vận chuyển lượng lớn hàng hóa bằng đường sắt”.<br />
					<br />
					Cũng theo ông Đảng, khi tuyến đường sắt Biên Hòa - Vũng Tàu, tuyến đường sắt đi miền Tây, Cảng hàng không quốc tế Long Thành được xây dựng và đi vào khai thác thì Trảng Bom sẽ là nơi có logistics rất phát triển.<br />
					<br />
					<strong>Kết nối vùng để phát triển</strong><br />
					<br />
					PGS-TS.Nguyễn Văn Thư, Hiệu trưởng Trường đại học giao thông vận tải TP.Hồ Chí Minh, cho rằng cần ưu tiên đầu tư các tuyến đường giao thông huyết mạch, kết nối với giao thông vùng. Đồng thời đầu tư kho, cảng ở những nơi phù hợp để vận chuyển hàng hóa từ các khu công nghiệp, cụm công nghiệp đến các cảng hoặc ngược lại sẽ rút ngắn được nhiều thời gian và chi phí về logistics cho doanh nghiệp, từ đó nâng cao khả năng cạnh tranh của hàng hóa Việt Nam.<br />
					<br />
				“Đầu tư phát triển đường sắt, đường thủy và cảng, kho tốt sẽ giảm rất nhiều chi phí cho doanh nghiệp và giảm áp lực cho đường bộ và giải quyết tình trạng kẹt xe ngày càng nhiều vì một đoàn tàu lửa có thể chở hàng gấp vài trăm chiếc xe ô tô” - ông Thư nhấn mạnh.</div>
