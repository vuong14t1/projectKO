				<div style="text-align: center;">
					<div style="text-align: justify;">Nhiều chuyên gia về quy hoạch đều có chung nhận xét, phát triển khu vực Trảng Bom - Long Thành - Nhơn Trạch thành trung tâm logistics vùng TP.Hồ Chí Minh là rất phù hợp. Vì đây là nơi tập trung nhiều tuyến đường cao tốc, quốc lộ, đường sắt, đường thủy và tới đây là đường hàng không. Do đó, tỉnh nên căn cứ vào quy hoạch vùng để xây dựng quy hoạch của tỉnh và quy hoạch chi tiết của từng địa phương trên để sớm có kế hoạch đầu tư, phát triển nhanh.</div>
					<div style="text-align: justify;"><br />
					</div>
					<div style="text-align: justify;">Phó chủ tịch UBND tỉnh Trần Văn Vĩnh cho biết: “3 địa phương trên sau này sẽ phát triển thành trung tâm logistics của vùng. Trong quy hoạch xây dựng, sử dụng đất tỉnh đều căn cứ vào quy hoạch vùng thực hiện để sau này sẽ kết nối với giao thông vùng”.</div>
					<div style="text-align: justify;"><br />
					</div>
					<div style="text-align: justify;">Hiện các địa phương trên đều dựa vào quy hoạch để mời gọi các nhà đầu tư vào lĩnh vực logistics. Đây cũng là lĩnh vực thời gian gần đây được nhiều doanh nghiệp trong nước cũng như nước ngoài quan tâm và đang muốn rót vốn đầu tư.</div>
					<div style="text-align: justify;"><br />
					</div>
					<div style="text-align: justify;">Theo ông K. Srika Reddy, Tổng lãnh sự Ấn Độ tại TP.Hồ Chí Minh, gần đây nhiều doanh nghiệp Ấn Độ đã đến Đồng Nai tìm hiểu quy hoạch và lĩnh vực mời gọi đầu tư và có dự tính sẽ đầu tư vào lĩnh vực logistics vì tỉnh rất có tiềm năng.</div>
